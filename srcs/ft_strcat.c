#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	int		i;
	size_t	size;

	size = ft_strlen((char *)dest);
	i = 0;
	while (src[i])
	{
		dest[size + i] = src[i];
		i++;
	}
	dest[size + i] = '\0';
	return (dest);
}
